				<h3>Las luces de la expresión corporal. Ventajas y posibilidades de los «Proyectos de Aprendizaje Expresivos» en Educación Física en Secundaria.Mª Teresa Archilla Prat, Darío Pérez Brunicardi (2017)</h3>
				<p>La expresión corporal debería de ser vital para el proceso educativo de una persona. Mediante ella y las distintas formas de trabajarla, el alumno o la alumna pueden experimentar un sinfín de sensaciones a la vez que conlleva multitud de beneficios. Este estudio es una propuesta didáctica basada en el aprendizaje por proyectos expresivos.</p>
