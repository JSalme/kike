				<h3>Elementos del arte urbano Hip - Hop para fortalecer el pensamiento fluido, flexible y elaborado en los alumnos del curso 705 del Colegio Roberto Velandia. Walter Andrés Delgadillo Cañon (2017)</h3>
				<p>Actualmente, la cultura Hip-Hop ha experimentado un auge dentro de la sociedad mundial, seguramente viviendo en varias de sus distintas modalidades de arte unos niveles de popularidad entre los jóvenes y adolescentes nunca antes vistos. Esto puede ser una magnifica herramienta para acercarnos a ellos. Este trabajo, refleja la multitud de beneficios que traen consigo los distintos elementos de esta cultura, y muestra una manera de como implementarse dentro de las aulas. </p>
