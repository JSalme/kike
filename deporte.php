			<p>Desde la llegada de la democracia la EF se ha basado esencialmente en la práctica de deporte y, dentro de esta, básicamente deportes tradicionales, con prácticas excesivamente analíticas o fuera de un contexto realde juego. Esto la ha convertido en poco motivante para buena parte del alumnado. Por ello este artículo mira hacia los deportes alternativos y nos enseña  como el alumnado puede ser capaz de crear nuevos deportes</p>
